/* exported last */
// declare a new function named 'last' with one parameter'array'
// access the last element of the array by using the length property on the array and subtracting it by 1. the result of that will be used as the index of the array
// return the last element of array

function last(array) {
  // return the last element of the array
  return array[array.length - 1];
}
